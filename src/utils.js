const parseCoordsArray = (coordsString) => {
  const trimmedString = coordsString.slice(2, -3);
  const pairs = trimmedString.split("],[");
  const coordsArray = pairs.map((pair) => {
    return pair.split(",").map(coord => parseFloat(coord.trim()));
  });
  return coordsArray;
}

export { parseCoordsArray };


